const fs = require("fs");
const path = require("path");
const childProcess = require("child_process");
const { loadRuntimeItemCatalog } = require("../content/runtime-item-catalog");
const { getProjectRoot } = require("./pixel-project");

function deriveAssetIdFromImagePath(iconPath) {
  const name = path.basename(iconPath, path.extname(iconPath));
  return name.replace(/-pixel$/i, "");
}

function importLegacyPng(projectRoot, assetId, iconPath) {
  const scriptPath = path.join(projectRoot, "tools", "pixel", "import-png-to-pixelsource.ps1");
  const sourcePath = path.join(projectRoot, iconPath.replace(/^\.\//, ""));
  childProcess.execFileSync(
    "powershell",
    [
      "-NoProfile",
      "-ExecutionPolicy",
      "Bypass",
      "-File",
      scriptPath,
      "-InputPath",
      sourcePath,
      "-AssetId",
      assetId
    ],
    {
      cwd: projectRoot,
      stdio: "inherit"
    }
  );
}

function rewriteIconObjects(itemCatalogPath) {
  const source = fs.readFileSync(itemCatalogPath, "utf8");
  let rewritten = source;

  rewritten = rewritten.replace(
    /icon:\s*\{\s*kind:\s*'sprite',\s*key:\s*'([^']+)'\s*\}/g,
    function (_, assetId) {
      return `icon: { kind: 'pixel', assetId: '${assetId}' }`;
    }
  );

  rewritten = rewritten.replace(
    /icon:\s*\{\s*kind:\s*'image',\s*path:\s*'([^']+)'\s*\}/g,
    function (_, iconPath) {
      const assetId = deriveAssetIdFromImagePath(iconPath);
      return `icon: { kind: 'pixel', assetId: '${assetId}' }`;
    }
  );

  rewritten = rewritten.replace(
    /function resolveIcon\(def, makeIconSprite, makeIconFromImage, assetVersionTag\) \{[\s\S]*?return '';\r?\n    \}/,
    [
      "function resolveIcon(def, makeMissingIconSprite, makeIconFromImage, assetVersionTag) {",
      "        if (!def || !def.icon) return '';",
      "        if (def.icon.kind === 'pixel') {",
      "            const assetId = typeof def.icon.assetId === 'string' ? def.icon.assetId.trim() : '';",
      "            if (!assetId) return makeMissingIconSprite();",
      "            const taggedPath = `./assets/pixel/${assetId}.png?v=${assetVersionTag}`;",
      "            return makeIconFromImage(taggedPath);",
      "        }",
      "        return makeMissingIconSprite();",
      "    }"
    ].join("\n")
  );

  rewritten = rewritten.replace(
    /function buildItemDb\(makeIconSprite, makeIconFromImage, assetVersionTag\) \{/,
    "function buildItemDb(makeMissingIconSprite, makeIconFromImage, assetVersionTag) {"
  );

  rewritten = rewritten.replace(
    /icon:\s*resolveIcon\(def, makeIconSprite, makeIconFromImage, assetVersionTag\)/,
    "icon: resolveIcon(def, makeMissingIconSprite, makeIconFromImage, assetVersionTag)"
  );

  fs.writeFileSync(itemCatalogPath, rewritten, "utf8");
}

function rewriteDraftItemJsons(projectRoot) {
  const itemsDir = path.join(projectRoot, "content", "items");
  const files = fs.readdirSync(itemsDir)
    .filter((name) => name.toLowerCase().endsWith(".json"))
    .filter((name) => !name.startsWith("_"))
    .filter((name) => name !== "runtime-item-catalog.json");

  for (let i = 0; i < files.length; i += 1) {
    const filePath = path.join(itemsDir, files[i]);
    const payload = JSON.parse(fs.readFileSync(filePath, "utf8"));
    if (!payload.assets || typeof payload.assets !== "object") continue;
    const assetId = payload.id;
    payload.assets.icon = `assets/pixel/${assetId}.png`;
    payload.assets.model = `assets/models/${assetId}.obj`;
    payload.assets.groundModel = `assets/models/${assetId}-ground.obj`;
    if (payload.notes === "Auto-generated by tool:item:create") {
      payload.notes = "Draft metadata aligned to the pixel source pipeline";
    }
    fs.writeFileSync(filePath, JSON.stringify(payload, null, 2) + "\n", "utf8");
  }
}

function main() {
  const projectRoot = getProjectRoot();
  const runtime = loadRuntimeItemCatalog(projectRoot);
  const itemDefs = runtime.itemDefs;
  const itemIds = Object.keys(itemDefs);
  const importedImageAssetIds = {};

  childProcess.execFileSync(
    "node",
    [path.join(projectRoot, "tools", "pixel", "migrate-svg-sprites-to-source.js")],
    { cwd: projectRoot, stdio: "inherit" }
  );

  for (let i = 0; i < itemIds.length; i += 1) {
    const icon = itemDefs[itemIds[i]] && itemDefs[itemIds[i]].icon;
    if (!icon || icon.kind !== "image" || typeof icon.path !== "string") continue;
    const assetId = deriveAssetIdFromImagePath(icon.path);
    if (importedImageAssetIds[assetId]) continue;
    importLegacyPng(projectRoot, assetId, icon.path);
    importedImageAssetIds[assetId] = true;
  }

  rewriteIconObjects(path.join(projectRoot, "src", "js", "content", "item-catalog.js"));
  rewriteDraftItemJsons(projectRoot);

  childProcess.execFileSync(
    "node",
    [path.join(projectRoot, "tools", "pixel", "normalize-pixel-sources.js")],
    { cwd: projectRoot, stdio: "inherit" }
  );

  childProcess.execFileSync(
    "node",
    [path.join(projectRoot, "tools", "pixel", "build-pixel-assets.js"), "--all"],
    { cwd: projectRoot, stdio: "inherit" }
  );

  console.log(`Migrated runtime item icons to pixel assets (${Object.keys(importedImageAssetIds).length} PNG-backed sources imported).`);
}

if (require.main === module) {
  try {
    main();
  } catch (error) {
    console.error(`ERROR: ${error.message}`);
    process.exit(1);
  }
}
